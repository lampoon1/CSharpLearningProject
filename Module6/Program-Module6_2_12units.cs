Configure and launch the debugger: The Run menu and RUN AND DEBUG view can both be used to configure and launch debug sessions.
Examine application state: The RUN AND DEBUG view includes a robust interface that exposes various aspects of your application 
state during a debug session.
Runtime execution control: The Debug toolbar provides high-level runtime controls during code execution.

Recap
Here are a few important things to remember from this unit:

The Visual Studio Code user interface can be used to configure, start, and manage debug sessions. 
The launch.json file contains the launch configurations for your application.
The Run menu provides easy access to common run and debug commands grouped into six sections.
The RUN AND DEBUG view provides access to runtime tools, including the Run and Debug controls panel. 
The sections of the RUN AND DEBUG view are VARIABLES, WATCH, CALL STACK, and BREAKPOINTS.
The Debug toolbar provides execution controls while your application is running such as pause/continue, 
step over, step into, step out, restart and stop.
The DEBUG CONSOLE is used to display messages from the debugger. The DEBUG CONSOLE can also display console 
output from your application.
